# list is an ordered sequence of items, most used datatypes
list_of_numbers = [1,2,3,4,5]
print(type(list_of_numbers))

# tuple is an ordered sequence of items same as list
# the only difference is tuples are immutable 
tuple_of_numbers = (1,2,3,4,5,6)
print(type(tuple_of_numbers))

# a set is unordered collection of unique items
a_set_of_numbers = {1,2,3,4,5,6,7}
print(type(a_set_of_numbers))


# a dictionary is unordered collection of key value 
number_dict = {'name':'alex', 'age':23, 'gender':'male'}